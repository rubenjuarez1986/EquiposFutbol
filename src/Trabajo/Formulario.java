    private void btnMostrarSAXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarSAXActionPerformed
        //Recupero el fichero
        if (getArchivo() == null) {
            this.labelNombreArchivo.setText("Vuelve a seleccionar el fichero");
        } else {
            //Abro el fichero
            if (sax.abrir_XML_SAX(getArchivo()) == -1) {
                this.labelNombreArchivo.setText("Error al crear el objeto SAX");
                this.btnMostrarSAX.setEnabled(false);

            } else {
                this.labelNombreArchivo.setText("Se ha abierto el archivo para SAX");
                this.btnMostrarSAX.setEnabled(true);
            }
        }
        //Recorro y muestro el texto
        this.jTextAreaSalida.setText(sax.recorrerSAX(getArchivo()));
    }//GEN-LAST:event_btnMostrarSAXActionPerformed

    private void btnMostrarJAXBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarJAXBActionPerformed
        if (getArchivo() == null) {
            this.labelNombreArchivo.setText("Vuelve a seleccionar el fichero");
        } else {
            if (jaxb.abrir_XML_JAXB(getArchivo()) == -1) {
                this.labelNombreArchivo.setText("Error al crear el objeto JAXB");
                this.btnMostrarSAX.setEnabled(false);
            } else {
                this.labelNombreArchivo.setText("Se ha abierto el archivo para JAXB");
                this.btnMostrarSAX.setEnabled(false);
            }
        }

        this.jTextAreaSalida.setText(jaxb.recorrerJAXB());
    }//GEN-LAST:event_btnMostrarJAXBActionPerformed

    private void btnModificarPosicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarPosicionActionPerformed
        if (jaxb.modificarPosicionTabla(this.jTextFieldPosicionAntigua.getText().trim(), this.jTextFieldPosicionNueva.getText()) == -1) {
            labelNombreArchivo.setText("Error al modificar posición");
        } else {
            //Pinta otra vez el texto con la nueva posición que he puesto 
            this.jTextAreaSalida.setText(jaxb.recorrerJAXB());
            labelNombreArchivo.setText("Esta bien modificada la posición");
        }
    }//GEN-LAST:event_btnModificarPosicionActionPerformed

