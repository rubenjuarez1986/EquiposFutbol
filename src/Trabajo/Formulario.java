    private void btnAgregarDomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarDomActionPerformed
        if (dom.annadirDom(this.jTextFieldEstadio.getText(), this.jTextFieldPresidente.getText(), this.jTextFieldNombre.getText(), this.jTextFieldCiudad.getText(),
                this.jTextFieldFundacion.getText(), this.jTextFieldPosicion_Tabla.getText(), this.jTextFieldPosicion.getText(), this.jTextFieldNombreJugador.getText(), this.jTextFieldNacionalidad.getText(), this.jTextFieldLiga.getText()) == 1) {
            this.labelNombreArchivo.setText("Error al añadir el equipo");

        } else {
            dom.guardarDOM(getArchivo());
            this.labelNombreArchivo.setText("Equipo añadido correctamente");
            dom.abrir_XML_DOM(getArchivo());
            jTextAreaSalida.setText(dom.recorrer_DOM_y_mostrar());
        }
    }//GEN-LAST:event_btnAgregarDomActionPerformed

